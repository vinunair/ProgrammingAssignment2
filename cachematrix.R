## makeCacheMatrix computes the inverse of the matrix and puts it in a cache
## cacheSolve decides to return the inverse of a matrix from cache if available, else
## calls makeCacheMatrix

## Computes the inverse of the matrix 

makeCacheMatrix <- function(x = matrix()) {
  m<-NULL
  set<-function(y){
    x<<-y
    m<<-NULL
  }
  get<-function() x
  setmatrix<-function(solve) m<<- solve
  getmatrix<-function() m
  list(set=set, get=get,
       setmatrix=setmatrix,
       getmatrix=getmatrix)
  
}


## Returns the inverse of matrix from the cache if available, else 
## calls makeCacheMatrix to compute again

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m<-x$getmatrix()
  if(!is.null(m)){
    message("getting cached data")
    return(m)
  }
  matrix<-x$get()
  m<-solve(matrix, ...)
  x$setmatrix(m)
  m
}
